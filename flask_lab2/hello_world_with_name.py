"""
Реализуйте endpoint /hello-world/<имя>, который возвращает строку «Привет, <имя>. Хорошей пятницы!».
Вместо хорошей пятницы endpoint должен уметь желать хорошего дня недели в целом, на русском языке.

Пример запроса, сделанного в субботу:

/hello-world/Саша  →  Привет, Саша. Хорошей субботы!
"""

from flask import Flask
from datetime import datetime

app = Flask(__name__)

# Используем кортеж для хранения названий дней недели (это наиболее оптимальный способ по памяти)
# Названия дней в родительном падеже для корректного склонения
WEEKDAYS = (
    "понедельника",
    "вторника",
    "среды",
    "четверга",
    "пятницы",
    "субботы",
    "воскресенья"
)


@app.route('/hello-world/<name>')
def hello_world(name):
    # Получаем текущий день недели (0 - понедельник, 6 - воскресенье)
    weekday = datetime.today().weekday()

    # Формируем сообщение с учетом текущего дня недели
    message = f"Привет, {name}. Хорошего {WEEKDAYS[weekday]}!"

    return message


if __name__ == '__main__':
    app.run(debug=True)